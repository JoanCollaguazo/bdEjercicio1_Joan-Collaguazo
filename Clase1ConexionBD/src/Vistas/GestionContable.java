    private void btnBuscarPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPersonaActionPerformed
        //MEQUEDO AQUI!!!!!!!!!------------------------------------------
        if (comboParametrodeBuqueda.getSelectedItem().equals("Cedula")) {
            List<Persona> personas = new ArrayList<>();
            personas.add(controladorPersona.obtenerPersonCedula(txtParametroBusqueda.getText()));
            ModelTablePersona.setPersonas(personas);
            ModelTablePersona.fireTableDataChanged();

        }
        if (comboParametrodeBuqueda.getSelectedItem().equals("Nombre")) {
            List<Persona> personaNombre = controladorPersona.obtenerPersonaNombre(txtParametroBusqueda.getText());
            ModelTablePersona.setPersonas(personaNombre);
            ModelTablePersona.fireTableDataChanged();

        }
        if (comboParametrodeBuqueda.getSelectedItem().equals("Apellidos")) {
            List<Persona> personaApellidos = controladorPersona.obtenerPersonaApellidos(txtParametroBusqueda.getText());
            ModelTablePersona.setPersonas(personaApellidos);
            ModelTablePersona.fireTableDataChanged();
        }

        if (comboParametrodeBuqueda.getSelectedItem().equals("Telefono")) {
            List<Persona> personaTelefono = controladorPersona.obtenerPersonaTelefono1(txtParametroBusqueda.getText());
            ModelTablePersona.setPersonas(personaTelefono);
            ModelTablePersona.fireTableDataChanged();
        }

        if (comboParametrodeBuqueda.getSelectedItem().equals("Correo")) {
            List<Persona> personaCorreo = controladorPersona.obtenerPersonaCorreo(txtParametroBusqueda.getText());
            ModelTablePersona.setPersonas(personaCorreo);
            ModelTablePersona.fireTableDataChanged();
        }
    }//GEN-LAST:event_btnBuscarPersonaActionPerformed

